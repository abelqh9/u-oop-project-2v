    private void addClienteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addClienteButtonActionPerformed
    
         
        try{
            int newClientId = Integer.parseInt(this.addClienteIdInput.getText());
            int newClientNumDoc = Integer.parseInt(this.addClienteNumDocInput.getText());
            int newClientEdad = Integer.parseInt(this.addClienteEdadInput.getText());
            int newClientCelular = Integer.parseInt(this.addClienteCelularInput.getText());
            
            // creando cliente
            Cliente newCliente = new Cliente(
          this.addClienteCorreoInput.getText(),
             newClientId,
        this.addClienteTipoDocInput.getText(),
       newClientNumDoc,
          this.addClienteNombreInput.getText(),
           this.addClienteSexoInput.getText(),
           newClientEdad,
         newClientCelular
            );
            
            // creando registro para la tabla
            String newClienteData[] = {
                this.addClienteIdInput.getText(),
                this.addClienteTipoDocInput.getText(),
                this.addClienteNumDocInput.getText(),
                this.addClienteNombreInput.getText(),
                this.addClienteSexoInput.getText(),
                this.addClienteEdadInput.getText(),
                this.addClienteCelularInput.getText(),
                this.addClienteCorreoInput.getText()
            };
            
            // creando variable para la tabla a seleccionada
            javax.swing.JTable selectedClientTable;
            Mesa selectedClientMesa;
            switch(this.mesaADondeAñadirCliente) {
                case 1 -> { selectedClientTable = this.ClientTable1; selectedClientMesa = mesa1; }
                case 2 -> { selectedClientTable = this.ClientTable2; selectedClientMesa = mesa2; }
                case 3 -> { selectedClientTable = this.ClientTable3; selectedClientMesa = mesa3; }
                default -> { selectedClientTable = this.ClientTable1; selectedClientMesa = mesa1; }
            }
            
            // añadiendo cliente al registro de la mesa
            selectedClientMesa.getListaClientes().add(newCliente);
            
            // añadiendo row a la tabla
            DefaultTableModel clientTableModel = (DefaultTableModel)selectedClientTable.getModel();
            clientTableModel.addRow(newClienteData);
            
            // ocultando modal
            this.addClientejDialog.setVisible(false);
            
        }catch (NumberFormatException e){
            System.out.println(e);
            System.out.println("falló la creación del new cliente");
        }
    }//GEN-LAST:event_addClienteButtonActionPerformed

