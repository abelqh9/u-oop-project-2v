    private void setComidaFormButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_setComidaFormButtonActionPerformed
        // TODO add your handling code here:
        
        System.out.print(this.meseroSelect.getSelectedItem());
        
        javax.swing.JTable selectedPlatoTable;
        Mesa selectedPlatoMesa;
        
        switch(this.mesaADondeAñadirPlato) {
            case 1 -> { selectedPlatoTable = this.tablaComida1 ; }
            case 2 -> { selectedPlatoTable = this.tablaComida2; }
            case 3 -> { selectedPlatoTable = this.tablaComida3; }
            default -> { selectedPlatoTable = this.tablaComida1; }
        }
        
        switch(this.mesaADondeAñadirPlato) {
            case 1 -> { selectedPlatoMesa = mesa1 ; }
            case 2 -> { selectedPlatoMesa = mesa2; }
            case 3 -> { selectedPlatoMesa = mesa3; }
            default -> { selectedPlatoMesa = mesa1; }
        }
        
        for (int i = 0; i < PlatosDisponibles.size(); i++ ){
            if(this.comidaComboBox1.getSelectedItem().equals(PlatosDisponibles.get(i).getNombre())){
            
                selectedPlatoMesa.getListaPlatos().add(PlatosDisponibles.get(i));

                String platoData[] = {
                    String.valueOf(PlatosDisponibles.get(i).getId()),
                    PlatosDisponibles.get(i).getNombre(),
                    String.valueOf(PlatosDisponibles.get(i).getPrecio()),
                    PlatosDisponibles.get(i).getSabor(),
                    "----"
                };

                // añadiendo row a la tabla
                DefaultTableModel platoTableModel = (DefaultTableModel) selectedPlatoTable.getModel();
                platoTableModel.addRow(platoData);
            }
        }
        
        for (int i = 0; i < bebidasDisponibles.size(); i++ ){
            if(this.comidaComboBox1.getSelectedItem().equals(bebidasDisponibles.get(i).getNombre())){
            
                selectedPlatoMesa.getListaBebidas().add(bebidasDisponibles.get(i));

                String bebidaData[] = {
                    String.valueOf(bebidasDisponibles.get(i).getId()),
                    bebidasDisponibles.get(i).getNombre(),
                    String.valueOf(bebidasDisponibles.get(i).getPrecio()),
                    "----",
                    bebidasDisponibles.get(i).getTemperatura(),
                };

                // añadiendo row a la tabla
                DefaultTableModel bebidaTableModel = (DefaultTableModel) selectedPlatoTable.getModel();
                bebidaTableModel.addRow(bebidaData);
            }
        }
        
        //this.meserobutton.getText()
        this.setMeseroToTablejDialog.setVisible(false);
    }//GEN-LAST:event_setComidaFormButtonActionPerformed

